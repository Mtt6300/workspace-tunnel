package command

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "workspace-tunnel",
	Short: "port forward services from remote kuberneters services",
	Long: `A dynamic application designed for Kubernetes users, enabling them to easily configure
and port forward multiple services and pods from their Kubernetes cluster to local ports`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

